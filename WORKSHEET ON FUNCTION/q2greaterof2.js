function greater_two(a,b){
    if(a > b){
        return "First number is greater"
    }
    else{
        return "Second number is greater"
    }
}
let res = greater_two(10,9)
let res1 = greater_two(2,56)
console.log(res)
console.log(res1)
// ans 1 res = First number is greater
// ans 2 res1 = Second number is greater